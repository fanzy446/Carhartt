package test.weapon;

import java.util.ArrayList;
import java.util.List;

public class ClassTree {
	Integer nodeId;
	String name ;
	boolean leaf;
	String parent;
	List<ClassTree> children = new ArrayList<ClassTree>();
	public ClassTree(){
		
	}
	public ClassTree(Integer nodeId, String name, boolean leaf, String parent,
			List<ClassTree> children) {
		super();
		this.nodeId = nodeId;
		this.name = name;
		this.leaf = leaf;
		this.parent = parent;
		this.children = children;
	}
	public Integer getNodeId() {
		return nodeId;
	}
	public void setNodeId(Integer nodeId) {
		this.nodeId = nodeId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isLeaf() {
		return leaf;
	}
	public void setLeaf(boolean leaf) {
		this.leaf = leaf;
	}

	public List<ClassTree> getChildren() {
		return children;
	}
	public void setChildren(List<ClassTree> children) {
		this.children = children;
	}

	public String getParent() {
		return parent;
	}

	public void setParent(String parent) {
		this.parent = parent;
	}

}
