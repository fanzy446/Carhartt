package com.carhartt.man.model;

import java.sql.Timestamp;
import java.util.HashSet;
import java.util.Set;

public class Itemtemp implements java.io.Serializable {

	// Fields

	private Integer itemId;
	private Subclass subclass;
	private Broadclass broadclass;
	private String itemName;
	private Timestamp itemPubDate;
	private String itemPhoto;
	private Float itemPrice;
	private Integer itemCount;
	private String itemUnit;
	private String itemDesc;
	private Boolean state;
	private String remark;
	private Integer sale;
	private Set evaluates = new HashSet(0);
	private Set orderdetialses = new HashSet(0);
	private Set favoriteses = new HashSet(0);
	private Set shoppingcarts = new HashSet(0);

	// Constructors

	/** default constructor */
	public Itemtemp() {
	}

	/** minimal constructor */
	public Itemtemp(Subclass subclass, Broadclass broadclass, String itemName,
			Float itemPrice, Integer itemCount) {
		this.subclass = subclass;
		this.broadclass = broadclass;
		this.itemName = itemName;
		this.itemPrice = itemPrice;
		this.itemCount = itemCount;
	}

	/** full constructor */
	public Itemtemp(Subclass subclass, Broadclass broadclass, String itemName,
			Timestamp itemPubDate, String itemPhoto, Float itemPrice,
			Integer itemCount, String itemUnit, String itemDesc, String remark,
			Integer sale, 
			Set evaluates, Set orderdetialses, Set favoriteses,
			Set shoppingcarts) {
		this.subclass = subclass;
		this.broadclass = broadclass;
		this.itemName = itemName;
		this.itemPubDate = itemPubDate;
		this.itemPhoto = itemPhoto;
		this.itemPrice = itemPrice;
		this.itemCount = itemCount;
		this.itemUnit = itemUnit;
		this.itemDesc = itemDesc;
		this.remark = remark;
		this.sale = sale;
		this.evaluates = evaluates;
		this.orderdetialses = orderdetialses;
		this.favoriteses = favoriteses;
		this.shoppingcarts = shoppingcarts;
	}

	// Property accessors

	public Integer getItemId() {
		return this.itemId;
	}

	public void setItemId(Integer itemId) {
		this.itemId = itemId;
	}

	public Subclass getSubclass() {
		return this.subclass;
	}

	public void setSubclass(Subclass subclass) {
		this.subclass = subclass;
	}

	public Broadclass getBroadclass() {
		return this.broadclass;
	}

	public void setBroadclass(Broadclass broadclass) {
		this.broadclass = broadclass;
	}

	public String getItemName() {
		return this.itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public Timestamp getItemPubDate() {
		return this.itemPubDate;
	}

	public void setItemPubDate(Timestamp itemPubDate) {
		this.itemPubDate = itemPubDate;
	}

	public String getItemPhoto() {
		return this.itemPhoto;
	}

	public void setItemPhoto(String itemPhoto) {
		this.itemPhoto = itemPhoto;
	}

	public Float getItemPrice() {
		return this.itemPrice;
	}

	public void setItemPrice(Float itemPrice) {
		this.itemPrice = itemPrice;
	}

	public Integer getItemCount() {
		return this.itemCount;
	}

	public void setItemCount(Integer itemCount) {
		this.itemCount = itemCount;
	}

	public String getItemUnit() {
		return this.itemUnit;
	}

	public void setItemUnit(String itemUnit) {
		this.itemUnit = itemUnit;
	}

	public String getItemDesc() {
		return this.itemDesc;
	}

	public void setItemDesc(String itemDesc) {
		this.itemDesc = itemDesc;
	}

	public String getRemark() {
		return this.remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Set getEvaluates() {
		return this.evaluates;
	}

	public void setEvaluates(Set evaluates) {
		this.evaluates = evaluates;
	}

	public Set getOrderdetialses() {
		return this.orderdetialses;
	}

	public void setOrderdetialses(Set orderdetialses) {
		this.orderdetialses = orderdetialses;
	}

	public Set getFavoriteses() {
		return this.favoriteses;
	}

	public void setFavoriteses(Set favoriteses) {
		this.favoriteses = favoriteses;
	}

	public Set getShoppingcarts() {
		return this.shoppingcarts;
	}

	public void setShoppingcarts(Set shoppingcarts) {
		this.shoppingcarts = shoppingcarts;
	}

	public Boolean getState() {
		return state;
	}

	public void setState(Boolean state) {
		this.state = state;
	}

	public int getSale() {
		return sale;
	}

	public void setSale(int sale) {
		this.sale = sale;
	}

}
