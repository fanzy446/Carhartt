package com.carhartt.man.model;
// default package

/**
 * Verification entity. @author MyEclipse Persistence Tools
 */

public class Verification implements java.io.Serializable {

	// Fields

	private Short verificationId;
	private User user;
	private Long time;
	private String message;

	// Constructors

	/** default constructor */
	public Verification() {
	}

	/** full constructor */
	public Verification(User user, Long time, String message) {
		this.user = user;
		this.time = time;
		this.message = message;
	}

	// Property accessors

	public Short getVerificationId() {
		return this.verificationId;
	}

	public void setVerificationId(Short verificationId) {
		this.verificationId = verificationId;
	}

	public User getUser() {
		return this.user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Long getTime() {
		return this.time;
	}

	public void setTime(Long time) {
		this.time = time;
	}

	public String getMessage() {
		return this.message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}